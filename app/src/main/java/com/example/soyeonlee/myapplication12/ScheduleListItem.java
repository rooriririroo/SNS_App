package com.example.soyeonlee.myapplication12;

public class ScheduleListItem {

    private String inputTitle;
    private String inputStartDate;
    private String inputStartTime;
    private String inputEndDate;
    private String inputEndTime;
    private String inputSub;

    public String getInputTitle() {
        return inputTitle;
    }

    public void setInputTitle(String inputTitle) {
        this.inputTitle = inputTitle;
    }

    public String getInputStartDate() {
        return inputStartDate;
    }

    public void setInputStartDate(String inputStartDate) {
        this.inputStartDate = inputStartDate;
    }

    public String getInputStartTime() {
        return inputStartTime;
    }

    public void setInputStartTime(String inputStartTime) {
        this.inputStartTime = inputStartTime;
    }

    public String getInputEndDate() {
        return inputEndDate;
    }

    public void setInputEndDate(String inputEndDate) {
        this.inputEndDate = inputEndDate;
    }

    public String getInputEndTime() {
        return inputEndTime;
    }

    public void setInputEndTime(String inputEndTime) {
        this.inputEndTime = inputEndTime;
    }

    public String getInputSub() {
        return inputSub;
    }

    public void setInputSub(String inputSub) {
        this.inputSub = inputSub;
    }
}
